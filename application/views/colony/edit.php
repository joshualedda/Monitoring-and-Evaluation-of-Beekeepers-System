<script type="text/javascript">

var manageTableProduction;
var base_url = "<?php echo base_url(); ?>";
function createFunc()
{
          $("#createFormProduction")[0].reset();
          $("#createFormProduction .form-group").removeClass('has-error').removeClass('has-success');  
          $(".text-danger").remove();
}

function getNetIncome()
{
          document.getElementById("production_net").value=document.getElementById("production_income").value - document.getElementById("production_cost").value;
}
function editGetNetIncome()
{
          document.getElementById("edit_production_net").value=document.getElementById("edit_production_income").value - document.getElementById("edit_production_cost").value; 
}

$(document).ready(function() {

    //---> creation of the drop-down list Product
    $product = $('[id="product"]');    
    $.ajax({
        url: base_url+'production/fetchProductData',
        dataType: "JSON", 
        success: function (data) {
            $product.html('');
            $product.append('<option value="" hidden selected disabled>Select Product</option>')
            //iterate over the data and append a select option
            $.each(data, function (key, val) {
                $product.append('<option value="' + val.id + '">' + val.name + '</option>');
            });  
            
        }, 
        error: function () {
        //if there is an error append a 'none available' option
        $product.html('<option id="-1">none available</option>');
        }
    });

//---> Prepare the view list

  $("#productionBeekeeperNav").addClass('active');

  // initialize the datatable 
  manageTableProduction = $('#manageTableProduction').DataTable({
    'ajax': base_url+'production/fetchProductionBeekeeper/'+<?php echo $colony_data['id']; ?>,
    'language': {'url': "<?php echo $this->session->link_language; ?>"}, 
    'order': [[0, 'desc']]
  });

 //---> Submit the create form  

  $("#createFormProduction").unbind('submit').on('submit', function() {
    var form = $(this);

    // remove the text-danger
    $(".text-danger").remove();
    getNetIncome();
    $.ajax({
      url: form.attr('action'),
      type: form.attr('method'),
      data: form.serialize(), // /converting the form data into array and sending it to server
      dataType: 'json',
      success:function(response) {

        manageTableProduction.ajax.reload(null, false); 

        if(response.success === true) {

          // hide the modal
          $("#createModalProduction").modal('hide');

          // reset the form
          $("#createFormProduction")[0].reset();
          $("#createFormProduction .form-group").removeClass('has-error').removeClass('has-success');

        } else {

          if(response.messages instanceof Object) {
            $.each(response.messages, function(index, value) {
              var id = $("#"+index);

              id.closest('.form-group')
              .removeClass('has-error')
              .removeClass('has-success')
              .addClass(value.length > 0 ? 'has-error' : 'has-success');
              
              id.after(value);

            });
          } else {
            $("#messages").html('<div class="alert alert-warning alert-dismissible" role="alert">'+
              '<button type="button" class="close" data-dismiss="alert" aria-label="Close"><span aria-hidden="true">&times;</span></button>'+
              '<strong> <span class="glyphicon glyphicon-exclamation-sign"></span> </strong>'+response.messages+
            '</div>');
          }
        }
      }
    }); 

    return false;
  });

});

//---> Edit function

function editProduction(id)
{ 

          $("#editFormProduction")[0].reset();
          $("#editFormProduction .form-group").removeClass('has-error').removeClass('has-success');  
          $(".text-danger").remove();
    
  $.ajax({

    url: base_url + 'production/fetchProductionDataById/'+id,
    type: 'post',
    dataType: 'json',
    success:function(response) {
       $('[name="edit_product"]').val(response.product_id).trigger('change');
      // $("#edit_product").val(response.product_id);
       $("#edit_total_production").val(response.total_production);
       $("#edit_production_income").val(response.gross_income);
       $("#edit_production_cost").val(response.cost);
       $("#edit_production_net").val(response.net_income);      
       $("#edit_production_date").val(response.production_date);      

       // submit the update form
       $("#editFormProduction").unbind('submit').bind('submit', function() {
          var form = $(this);

      // remove the text-danger
      $(".text-danger").remove();
      editGetNetIncome();
      $.ajax({
        url: form.attr('action') + '/' + id,
        type: form.attr('method'),
        data: form.serialize(), // converting the form data into array and sending it to server
        dataType: 'json',
        success:function(response) {

          manageTableProduction.ajax.reload(null, false); 

          if(response.success === true) {

            // hide the modal
            $("#editModalProduction").modal('hide');
            // reset the form 
            $("#editFormProduction .form-group").removeClass('has-error').removeClass('has-success');

          } else {

            if(response.messages instanceof Object) {
              $.each(response.messages, function(index, value) {
                var id = $("#"+index);

                id.closest('.form-group')
                .removeClass('has-error')
                .removeClass('has-success')
                .addClass(value.length > 0 ? 'has-error' : 'has-success');
                
                id.after(value);

              });
            } else {
              $("#messages").html('<div class="alert alert-warning alert-dismissible" role="alert">'+
                '<button type="button" class="close" data-dismiss="alert" aria-label="Close"><span aria-hidden="true">&times;</span></button>'+
                '<strong> <span class="glyphicon glyphicon-exclamation-sign"></span> </strong>'+response.messages+
              '</div>');
            }
          }
        }
      }); 

      return false;
    });

    }
  });
}

